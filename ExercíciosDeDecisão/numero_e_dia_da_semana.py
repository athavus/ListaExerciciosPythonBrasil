numero_da_semana = int(input('Digite o número correspondente a o dia da semana: '))

if numero_da_semana == 1:
    print('Domingo')
elif numero_da_semana == 2:
    print('Segunda')
elif numero_da_semana == 3:
    print('Terça')
elif numero_da_semana == 4:
    print('Quarta')
elif numero_da_semana == 5:
    print('Quinta')
elif numero_da_semana == 6:
    print('Sexta')    
elif numero_da_semana == 7:
    print('Sábado')
else:
    print('Valor inválido')
